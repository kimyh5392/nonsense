                $('.jumbotron span').addClass('active');
            }, 1800);
            
            $(window).on('scroll', function() {
                $('.effect').each(function(index, elem) {
                    if ($(window).scrollTop() > $(elem).offset().top - ($(window).height() / 2)) {
                        $(elem).addClass('action');
                    }
                });
            });
            
            // work-slider
            $('.work-slider').slick({
                slidesToShow: 1,
                slidesToScroll: 1,
                dots: false,

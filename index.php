                slidesToShow: 4,
                slidesToScroll: 1,
                dots: false,
                prevArrow: $('.btn-prev'),
                nextArrow: $('.btn-next'),
                autoplay: true,
                autoplaySpeed: 4000,
                responsive: [{
                breakpoint: 1024,
                    settings: {
                        slidesToShow: 1,
